# Creating the tuple with the numbers in it.
numbers = (2, 4, 6, 8, 10)

# Operations
total_sum = sum(numbers)
maximum = max(numbers)
minimum = min(numbers)
length = len(numbers)

# Repeating the tuple
repeated_tuple = numbers * 2

print("Original tuple:", numbers)
print("Sum:", total_sum)
print("Maximum:", maximum)
print("Minimum:", minimum)
print("Length:", length)
print("Repeated tuple:", repeated_tuple)